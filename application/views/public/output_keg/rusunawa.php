            <h2 id="Landasan Hukum">Landasan Hukum</h2>
            <p>Era baru yang lebih menjanjikan bagi pengembangan program rusun baru saja dimulai. Indonesa kini telah memiliki Undang-Undang (UU) Rumah
                Susun (rusun) baru. UU No. 20 tahun 2011 yang telah disyahkan Dewan Perwakilan Rakyat (DPR) dalam Sidang Paripurna tanggal 18 Oktober 2011
                ini menggantikan UU No. 16 tahun 1985 yang sudah tidak sesuai dengan perkembangan hukum, kebutuhan setiap individu dan partisipasi masyarakat
                serta tanggungjawab dan kewajiban negara dalam penyelenggaraan rusun. Disamping dinamika perubahan masyarakat, globalisasi dan budaya yang
                turut mempengaruhi dan menjadikan Undang-Undang sebelumnya tidak lagi memadai sebagai pedoman dalam pengaturan penyelenggaraan rusun.</p>
            <p>Undang-Undang yang memperkuat konsepsi program penanganan Perumahan dan Permukiman Kumuh dan penyelenggaraan rusunawa
                yang telah dilakukan oleh Direktorat Jenderal Cipta Karya dalam beberapa tahun terakhir ini jelas mengamanatkan penanganan terpadu lintas sektoral,
                mengingat penanganan masalah ini tidak mungkin diselesaikan oleh satu sektor saja. Dalam hal ini Pemerintah Kabupaten/Kota diamanatkan
                mempersiapkan diri terutama dalam menetapkan lokasi-lokasi kawasan kumuh dan merencanakan penanganan secara terpadu. Adapun Pemerintah
                Provinsi diharapkan perannya sebagai fasilitator yang membantu koordinasi, dan pembinaan dalam proses pelaksanaan fisiknya.</p>
            <p>Keberadaan Undang-Undang ini jelas telah memperkuat legitimasi serta menciptakan dasar hukum yang tegas berkaitan dengan penyelenggaraan
                rusun berdasarkan asas kesejahteraan, keadilan dan pemerataan, kenasionalan, keterjangkauan dan kemudahan, keefisienan dan kemanfaatan,
                kemandirian dan kebersamaan, kemitraan, keserasian dan keseimbangan, keterpaduan, kesehatan, kelestarian dan berkelanjutan, keselamatan,
                kenyamanan, dan kemudahan, serta keamanan, ketertiban, dan keteraturan.</p>

            <h2 id="Penataan Permukiman Kumuh">Rusunawa Sebagai Strategi Penataan Permukiman Kumuh</h2>						
            <p>Permukiman kumuh bisa diatasi dengan perencanaan kawasan dan implementasi yang
                terintegrasi dengan hunian vertikal sebagai salah satu solusinya. Kelangkaan lahan serta tingginya harga
                lahan di perkotaan telah mengisyaratkan pemangku kepentingan khususnya pemerintah, masyarakat
                dan swasta untuk mulai mempertimbangkan konsepsi hunian vertikal sebagai upaya efisiensi
                lahan yang juga akan berdampak pada penurunan biaya hidup masyarakat serta penghematan energi
                terutama dikaitkan dengan pembelanjaan dibidang transportasi.</p>
            <p>Rusunawa sebagai salah satu strategi penataan permukiman kumuh perkotaan membawa beberapa implikasi positif antara lain :</p>
            <ol>
                <li>Membantu mengatasi permasalahan pemukiman
                    kumuh perkotaan dengan penerapan urban
                    renewel atau peremajaan kota.</li>
                <li>Sebagai bentuk keberpihakan kepada
                    masyarakat berpenghasilan rendah (MBR) yang
                    belum atau tidak mampu menghuni rumah
                    milik.</li>
                <li>Menjamin kepastian dan keamanan tinggal
                    (secure tenure) terutama bagi komunitas
                    yang semula menghuni lingkungan dan atau
                    kawasan ilegal.</li>
                <li>Penggunaan lahan yang efisien akan
                    berdampak pada pelestarian lingkungan
                    karena memperluas daerah resapan air dan
                    Ruang Terbuka Hijau (RTH), serta memberikan
                    ruang/lahan untuk fungsi-fungsi sosial yang
                    bermanfaat bagi kehidupan dan kesejahteraan
                    komunitas yang tinggal dilingkungan tersebut.</li>
                <li>Teknik pembangunan fisik rusunawa
                    telah dikembangkan (diantaranya dengan
                    sistem prototype dan sistem terkini) yang
                    mempercepat proses konstruksi yang dapat
                    diandalkan dalam efisiensi waktu, pengatasan
                    permasalahan runtuh dan tahan gempa.</li>
                <li>Bentuk bangunan vertikal menekankan pada
                    efisiensi pemanfaatan lahan.</li>
                <li>Konsentrasi hunian yang terpusat menciptakan
                    efisiensi dalam investasi dan pemeliharaan
                    infrastruktur perkotaan.</li>
                <li>Radius pencapaian yang relatif dekat dengan
                    pusat kota akan mengurangi pemborosan
                    biaya hidup keluarga dan penghematan energi
                    berkaitan dengan transportasi.</li>
            </ol>
            <h2 id="Penyelenggara Rusunawa">Direktorat jenderal Cipta Karya sebagai Penyelenggara Rusunawa</h2>
            <p>Keberadaan Undang-Undang No.1 tahun 2011
                tentang Perumahan Kawasan Permukiman yang
                menyempurnakan UU No. 4 tahun 1992 tentang
                Perumahan dan Permukiman, UU No. 20 tahun
                2011 tentang Rumah Susun menggantikan UU
                No. 16 tahun 1985 tentang Rumah Susun, telah
                memperkuat konsepsi program penanganan
                perumahan dan permukiman kumuh serta
                penyelenggaraan Rusunawa yang sudah dilakukan
                oleh Direktorat Jenderal Cipta Karya sejak tahun
                1993.</p>
            <p>UU No. 20/2011, mengkategorikan rumah susun
                komersial, umum, khusus dan Negara. Sebagian
                besar rusun yang dibangun oleh Kementerian PU
                sampai saat ini adalah rusun umum dan khusus
                yang mewajibkan pemerintah untuk bertanggung
                jawab dan memfasilitasi penyediaanya yang
                diperuntukan bagi MBR dengan cara sewa.</p>
            <p>Renstra Kementerian PU 2010-1014
                mengamanatkan pembangunan 250 Twin Block
                (TB) yang difokuskan untuk membantu Pemda
                dan menata permukiman perkotaan. Pada periode
                tahun 2010-2012 telah dibangun sebanyak 110
                TB, sisanya sebanyak 140 TB direncanakan akan
                dibangun pada periode tahun 2013-2014.</p>
            <p>Menurut UU No. 20/2011, penyelenggaraan rusun
                meliputi :</p>
            <ol>
                <li>Perencanaan, Pembangunan dan Penguasaan.</li>
                <li>Pemanfaatan, Pengelolaan dan Pemeliharaan.</li>
                <li>Perawatan, Pengendalian, Kelembagaan,
                    Pendanaan dan Sistem Pembiayaan.</li>
                <li>Peran serta masyarakat yang dilaksanakan
                    secara sistematis, terpadu, berkelanjutan dan
                    bertanggung jawab.</li>
            </ol>
